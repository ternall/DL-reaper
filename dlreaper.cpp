#include <bits/stdc++.h>
using namespace std;
using int64 = long long;
using u64 = uint64_t;
using i128 = __int128_t;
bool TRACE = true;
struct Timer {
    chrono::high_resolution_clock::time_point start;

    void begin() {
        start = chrono::high_resolution_clock::now();
    }

    double end() {
        auto stop = chrono::high_resolution_clock::now();
        return chrono::duration<double>(stop - start).count();
    }
};

// hash
struct FastHash {
    static u64 splitmix64(u64 x) {
        x += 0x9e3779b97f4a7c15;
        x = (x ^ (x >> 30)) * 0xbf58476d1ce4e5b9;
        x = (x ^ (x >> 27)) * 0x94d049bb133111eb;
        return x ^ (x >> 31);
    }

    size_t operator()(u64 x) const {
        static u64 seed = chrono::steady_clock::now().time_since_epoch().count();
        return splitmix64(x + seed);
    }
};

static inline int64 mod_pow(int64 a, int64 e, int64 mod) {
    i128 r = 1, x = a % mod;
    while (e) {
        if (e & 1) r = (r * x) % mod;
        x = (x * x) % mod;
        e >>= 1;
    }
    return (int64)r;
}

static inline int64 egcd(int64 a, int64 b, int64 &x, int64 &y) {
    if (!b) { x = 1; y = 0; return a; }
    int64 x1, y1;
    int64 g = egcd(b, a % b, x1, y1);
    x = y1;
    y = x1 - y1 * (a / b);
    return g;
}

static inline int64 mod_inv(int64 a, int64 mod) {
    int64 x, y;
    int64 g = egcd(a, mod, x, y);
    if (g != 1) return -1;
    x %= mod;
    return x < 0 ? x + mod : x;
}

